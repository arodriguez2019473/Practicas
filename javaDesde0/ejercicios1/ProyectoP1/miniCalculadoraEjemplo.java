import java.lang.Math;


public class miniCalculadoraEjemplo{
    
    public miniCalculadoraEjemplo(){
    
    
    }
    
    
    public double valorAbsoluto(double a){
    
        return Math.abs(a);
    }
    
    
    public double raizCuadrada(double b){
    
        return Math.sqrt(b);
    }
    
    
}
